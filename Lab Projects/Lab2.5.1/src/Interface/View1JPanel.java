    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAbnormalVitalSigns.getSelectedRow();

        if(selectedRow >= 0)
        {
            Airliner vs = (Airliner) tblAbnormalVitalSigns.getValueAt(selectedRow, 0);
            vsh.deleteVitals(vs);
            JOptionPane.showMessageDialog(null,"Vital Sign has been deleted successfully");
            populateTable();
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

    private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
        // TODO add your handling code here:
        confirmButton.setEnabled(true);
        int selectedrow = tblAbnormalVitalSigns.getSelectedRow();

        if(selectedrow >= 0)
        {
            Airliner vs = (Airliner) tblAbnormalVitalSigns.getValueAt(selectedrow, 0);
            vs.setAirlinerId((tempratureTextField.getText()));
           
            vs.setAirlinerName((bpTextField.getText()));
            vs.setContactNum((pulseTextField.getText()));
             vs.setEmail(dateTextField.getText());
            vs.setNumFlights(numberOfFlight.getText());

            JOptionPane.showMessageDialog(null, "vital Signs successfully updated");
            populateTable();
            setFieldEnabled(false);
            confirmButton.setEnabled(false);

        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please Select Any Row");

        }

    }//GEN-LAST:event_confirmButtonActionPerformed

