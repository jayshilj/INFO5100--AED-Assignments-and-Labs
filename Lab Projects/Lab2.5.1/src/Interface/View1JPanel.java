         public void populateTable()
    {
        DefaultTableModel dtm = (DefaultTableModel) tblAbnormalVitalSigns.getModel();
        dtm.setRowCount(0);
        
        for(Airliner vs: vsh.getVitalSignHistory() )
        {
            Object row[] = new Object[6];
            row[0]= vs;
            row[1]= vs.getAirlinerId();
            row[2] = vs.getAirlinerName();
            row[3] = vs.getContactNum();
            row[4] =vs.getEmail();
            row[5] = vs.getNumFlights();
            dtm.addRow(row);
        }
    }
